			<h2 class="title">...Welcome to SeaPal</h2>
			<div class="content">
				<p>
					<span class="subtitle">SeaPal</span> is an App for sailors and
					other water sport fanatics that support the skipper in his tasks
					for navigation, route planing and logbook keeping. The central view
					shows a map with the current position and forward line. Waypoint
					and routes can easily be created, distances and courses can quickly
					be determined and documented in the logbook. The touch interface
					allows for fast and intuitive interaction.
				</p>
				<p>Online maps are available from Google and OpenSeaMap, allong with
					satellite view. With these a trip can easily be planed and
					documented. In navigation the offline operation and the precision
					and depth of detail is of special importance. Thats why digital sea
					maps for selected areas can be downloaded and used offline.</p>
				<p>For the area of the Lake Constance excellent sea maps and local
					information (harbours, shipyards, shops, restaurants) provided by
					the IBN publishing house are available as extensions. Further areas
					are in preparation.</p>
				<p>The application was developed particularly for the iPad in a
					cooperation between the IBN publishing house and the HTWG Constance
					University. The specific advantages of a mobile device and the
					touch interface were taken into account just as well as the special
					requirements of sailors and skippers. As a result of this
					cooperation, SeaPal is made available for free. Extensions and sea
					maps can be purchased and loaded according to your area and needs.</p>
				<p>This cooperation is open to further extension. Should you be
					interested to develop or provide further extensions, please contact
					us.</p>
				<p>The application runs on the iPad and with reduced functionality
					on the iPhone. An iPad with 3G or 4G and GPS is recommended. For
					longer use an external power source (like a USP port of a radio) is
					required.</p>
	
